package metodos;

import javax.swing.JOptionPane;

public class Treinamento {

	public static void main(String[] args) {

		int peso = Integer.parseInt(JOptionPane.showInputDialog("Insira quantos quilos você tem"));

		while (peso <= 0) {
			mostraErroPeso();
			peso = Integer.parseInt(JOptionPane.showInputDialog("Insira quantos quilos você tem"));
		}
		;

		double altura = Double.parseDouble(JOptionPane.showInputDialog("Insira quantos metros você tem"));

		while (altura <= 0) {
			mostraErroAltura();
			altura = Double.parseDouble(JOptionPane.showInputDialog("Insira quantos metros você tem"));
		}
		;
		
		double imc = calculaIndice(peso,altura);
		verificaIndice(imc);
	};

	public static double calculaIndice(int peso, double altura) {

		double imc = peso / (altura * altura);

		return imc;

	};

	public static void verificaIndice(double imc) {

		if (imc < 17) {
			JOptionPane.showMessageDialog(null, "Você está muito abaixo do peso! Índice: " + imc);
		} else if (imc == 17 || imc <= 18.49) {
			JOptionPane.showMessageDialog(null, "Você está abaixo do peso! Índice: " + imc);
		} else if (imc <= 24.99) {
			JOptionPane.showMessageDialog(null, "Você está com o peso adequado! Índice: " + imc);
		} else if (imc <= 29.99) {
			JOptionPane.showMessageDialog(null, "Você está acima do peso! Índice: " + imc);
		} else if (imc <= 34.99) {
			JOptionPane.showMessageDialog(null, "VVocê está com obesidade tipo I! Índice: " + imc);
		} else if (imc <= 39.99) {
			JOptionPane.showMessageDialog(null, "Você está com obesidade tipo II! Índice: " + imc);
		} else if (imc >= 40) {
			JOptionPane.showMessageDialog(null, "Você está com obesidade tipo III! Índice: " + imc);
		}

	};

	public static void mostraErroPeso() {

		JOptionPane.showMessageDialog(null, "Peso inválido digitado!", "Erro", JOptionPane.INFORMATION_MESSAGE);
	};

	public static void mostraErroAltura() {

		JOptionPane.showMessageDialog(null, "Altura inválida digitada!", "Erro", JOptionPane.INFORMATION_MESSAGE);
	};
}
